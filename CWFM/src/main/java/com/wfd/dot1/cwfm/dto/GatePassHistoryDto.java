package com.wfd.dot1.cwfm.dto;

public class GatePassHistoryDto {
	
	private String gatePassId;
	private String gatePassAction;
	private String gatePassStatus;
	private String fromDate;
	private String toDate;
	private String updatedBy;
	public String getGatePassId() {
		return gatePassId;
	}
	public void setGatePassId(String gatePassId) {
		this.gatePassId = gatePassId;
	}
	public String getGatePassAction() {
		return gatePassAction;
	}
	public void setGatePassAction(String gatePassAction) {
		this.gatePassAction = gatePassAction;
	}
	public String getGatePassStatus() {
		return gatePassStatus;
	}
	public void setGatePassStatus(String gatePassStatus) {
		this.gatePassStatus = gatePassStatus;
	}
	public String getFromDate() {
		return fromDate;
	}
	public void setFromDate(String fromDate) {
		this.fromDate = fromDate;
	}
	public String getToDate() {
		return toDate;
	}
	public void setToDate(String toDate) {
		this.toDate = toDate;
	}
	public String getUpdatedBy() {
		return updatedBy;
	}
	public void setUpdatedBy(String updatedBy) {
		this.updatedBy = updatedBy;
	}
	
}


